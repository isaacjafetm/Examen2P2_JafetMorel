    private void jTabbedPane1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jTabbedPane1StateChanged
        // TODO add your handling code here:
        if (jTabbedPane1.getSelectedIndex() == 1) {
            DefaultListModel mod1 = new DefaultListModel();
            DefaultListModel mod2 = new DefaultListModel();
            adminVeh av = new adminVeh("./veh.txt");
            adminEns ae = new adminEns("./ens.txt");
            av.cargarArchivo();
            ae.cargarArchivo();
            for (int j = 0; j < ae.getListaPersonas().size(); j++) {
                mod1.addElement(ae.getListaPersonas().get(j));
            }
            for (int j = 0; j < av.getListaVehiculos().size(); j++) {
                mod2.addElement(av.getListaVehiculos().get(j));
            }
            jl_ens.setModel(mod1);
            jl_veh.setModel(mod2);
        }
    }//GEN-LAST:event_jTabbedPane1StateChanged

